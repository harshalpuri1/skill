// Modulus operator
let mod = a % b;
console.log("\nModulus of a / b = "+mod);

// Exponentiation operator
let exp = a ** b;
console.log("\nExponentiation of a ** b = "+exp);

// Increment operator
let inc = a++;
console.log("\nIncrement of a++ = "+inc);

// Decrement operator
let dec = a--;
console.log("\nDecrement of a-- = "+dec);

// Unary plus operator
let unaryPlus = +a;
console.log("\nUnary plus of +a = "+unaryPlus);

// Unary minus operator
let unaryMinus = -a;
console.log("\nUnary minus of -a = "+unaryMinus);

// Bitwise AND operator
let bitwiseAnd = a & b;
console.log("\nBitwise AND of a & b = "+bitwiseAnd);

// Bitwise OR operator
let bitwiseOr = a | b;
console.log("\nBitwise OR of a | b = "+bitwiseOr);

// Bitwise XOR operator
let bitwiseXor = a ^ b;
console.log("\nBitwise XOR of a ^ b = "+bitwiseXor);

// Bitwise NOT operator
let bitwiseNot = ~a;
console.log("\nBitwise NOT of ~a = "+bitwiseNot);

// Bitwise LEFT SHIFT operator
let bitwiseLeftShift = a << b;
console.log("\nBitwise LEFT SHIFT of a << b = "+bitwiseLeftShift);

// Bitwise RIGHT SHIFT operator
let bitwiseRightShift = a >> b;
console.log("\nBitwise RIGHT SHIFT of a >> b = "+bitwiseRightShift);

// Bitwise ZERO FILL RIGHT SHIFT operator
let bitwiseZeroFillRightShift = a >>> b;
console.log("\nBitwise ZERO FILL RIGHT SHIFT of a >>> b = "+bitwiseZeroFillRightShift);

// Comma operator
let comma = (a++, b++);
console.log("\nComma operator of a++, b++ = "+comma);

